// interfaces/response.ts

/**
 * Універсальна структура відповіді від бекенду
 * @template T - тип даних, що повертаються в полі data
 */
export interface IRes<T = unknown> {
  /** Основні дані відповіді */
  data: T;

  /** Опціональне повідомлення про помилку (якщо є) */
  error?: string;

  /** Опціональне повідомлення для користувача (наприклад, "Дані успішно збережено") */
  message?: string;
}