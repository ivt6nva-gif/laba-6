// interfaces/locations.ts

/**
 * Інформація про локацію (місце розташування сенсорів)
 */
export interface ILocation {
  /** Унікальний ідентифікатор локації */
  id: number;

  /** Назва локації (наприклад: "Офіс", "Склад №3", "Кімната 101") */
  name: string;

  /** Додатковий опис локації (опціонально, але часто корисно) */
  description: string;
}